    void port()
    {
        try
        {
          String query = jTextField1.getText();
          URL url = new URL(query);
          int port = url.getPort();
          JOptionPane.showMessageDialog(this,port);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
    }
    
