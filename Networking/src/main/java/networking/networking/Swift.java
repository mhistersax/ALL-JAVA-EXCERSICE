       void Ipaddress()
    {
        try
        {
          String query = jTextField1.getText();
         InetAddress ipAddress = InetAddress.getByName(query);
         String ip = ipAddress.getHostAddress();
          JOptionPane.showMessageDialog(this,ip);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e);
        }
    }
       
